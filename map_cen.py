        auth_configs = managerAU.availableAuthMethodConfigs()  # R√©cup√©rer toutes les configurations disponibles

        if not auth_configs:
            QMessageBox.warning(self.dlg, "Pas de configurations", "Aucune configuration d'authentification disponible.")
