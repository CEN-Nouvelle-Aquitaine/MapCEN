class LoadingThread(QThread):
    # Signaux pour d√©marrer et terminer le chargement
    started = pyqtSignal()
    finished = pyqtSignal()

    def __init__(self, plugin_instance, target_function):
        super().__init__()
        self.plugin_instance = plugin_instance
        self.target_function = target_function  # Function to run in the thread
        
    def run(self):
        self.started.emit()  # D√©marre l'animation
        self.target_function()  # Ex√©cute la fonction de chargement
        self.finished.emit()  # Termine l'animation

